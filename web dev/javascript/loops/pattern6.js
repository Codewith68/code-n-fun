  function upper(n){
        for(let i=1;i<=(n-1)/2;i++){
            let stars="";
            for(let j=1;j<=i;j++) stars+="*";
            for(let k=1;k<=n-(2*i);k++) stars+=" ";
            for(let j=1;j<=i;j++) stars+="*";
            console.log(stars);
        }
    }
    function Middel(n){
        let stars="";
        for(let i=1;i<=n;i++) stars+="*";
        console.log(stars);
    }
    function lower(n){
        for(let i=1;i<=(n-1)/2;i++){
            let stars="";
            for(let j=1;j<=(n-1)/2-i+1;j++) stars+="*";
            for(let k=1;k<=2*i-1;k++) stars+=" ";
            for(let j=1;j<=(n-1)/2-i+1;j++) stars+="*";
            console.log(stars);
        }
    }


